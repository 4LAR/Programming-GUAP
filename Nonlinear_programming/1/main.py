
import numpy as np
from scipy.optimize import linprog

# Коэффициенты целевой функции (стоимость кормов)
c = [9, 12, 10]  # цены на 1 кг корма I, II и III видов

# Ограничения по питательным веществам
# Необходимо получить не менее 60 ед. A, 50 ед. B и 12 ед. C
A = [
    [-1, -3, -4],  # для A
    [-2, -4, -2],  # для B
    [-1, -4, -3]   # для C
]
b = [-60, -50, -12]  # правые части неравенств

# Ограничения на неотрицательность (количество корма не может быть отрицательным)
x0_bounds = (0, None)  # корм I
x1_bounds = (0, None)  # корм II
x2_bounds = (0, None)  # корм III

# Решение задачи линейного программирования
res = linprog(c, A_ub=A, b_ub=b, bounds=[x0_bounds, x1_bounds, x2_bounds], method='highs')

# Вывод результатов
if res.success:
    print("Оптимальное количество корма I, II и III видов (в кг):")
    print(res.x)
    print("Минимальные затраты (в копейках):", res.fun)
else:
    print("Не удалось найти решение.")
