import numpy as np
from scipy import stats

# Данные
x = np.array([6.3, 13.8, 14.7, 9.1, 3.5, 1.2, 0.7, 0.2, 2.2, 2.6])
y = np.array([10.1, 9.5, 3.2, 2.0, 1.3, 2.8, 4.5, 6.2, 9.8, 11.7])

# 1. Найти оценки математических ожиданий
mean_x = np.mean(x)
mean_y = np.mean(y)

print(f"Математическое ожидание X: {mean_x:.4f}")
print(f"Математическое ожидание Y: {mean_y:.4f}")

# 2. Проверка нулевой гипотезы о равенстве математических ожиданий
# Для четных вариантов: H0: E(X) = E(Y) против H1: E(X) > E(Y)
# Для нечетных вариантов: H0: E(X) = E(Y) против H1: E(X) < E(Y)

# Выполняем t-тест
t_stat, p_value = stats.ttest_ind(x, y, alternative='less')

# Результаты
alpha = 0.05  # Уровень значимости
print(f"t-статистика: {t_stat:.4f}, p-значение: {p_value:.4f}")

if p_value < alpha:
    print("Отвергаем нулевую гипотезу: математическое ожидание X больше математического ожидания Y.")
else:
    print("Не отвергаем нулевую гипотезу: недостаточно доказательств, чтобы утверждать, что математическое ожидание X больше Y.")
